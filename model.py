class sub_pixel(nn.Module):
    def __init__(self, scale, act=False):
        super(sub_pixel, self).__init__()
        modules = []
        modules.append(nn.PixelShuffle(scale))
        self.body = nn.Sequential(*modules)
    def forward(self, x):
        x = self.body(x)
        return x
## Channel Attention (CA) Layer
class RC_CALayer(nn.Module):
    def __init__(self, channel, reduction=16):
        super(RC_CALayer, self).__init__()
        # global average pooling: feature --> point
        self.avg_pool = nn.AdaptiveAvgPool2d(1)
        # feature channel downscale and upscale --> channel weight
        self.conv_du = nn.Sequential(
                nn.Conv2d(channel, channel // reduction, 1, padding=0, bias=True),
                nn.ReLU(),
                nn.Conv2d(channel // reduction, channel, 1, padding=0, bias=True),
                nn.Sigmoid()
        )

    def forward(self, x):
        y = self.avg_pool(x)
        y = self.conv_du(y)
        return x * y

## Residual Channel Attention Block (RCAB)
class RCAB(nn.Module):
    def __init__(
        self, conv, n_feat, kernel_size, reduction,
        bias=True, bn=False, act=nn.ReLU(True), res_scale=1):

        super(RCAB, self).__init__()
        modules_body1 = []
        modules_ca = []
        for i in range(2):
            modules_body1.append(conv(n_feat, n_feat, kernel_size, bias=bias))
            if bn: modules_body1.append(nn.BatchNorm2d(n_feat))
            if i == 0: modules_body1.append(act)
        self.body1 = nn.Sequential(*modules_body1)
        self.body2 = nn.Sequential(
                nn.Conv2d(n_feat, n_feat, kernel_size,padding=1,bias=bias),
                act,
                nn.Conv2d(n_feat, n_feat, kernel_size,dilation=2, padding=2,bias=bias)       
        )
        self.body3 = nn.Sequential(
                nn.Conv2d(n_feat, n_feat, kernel_size,padding=1,bias=bias),
                act,
                nn.Conv2d(n_feat, n_feat, kernel_size,dilation=2, padding=2,bias=bias),
                act,
                nn.Conv2d(n_feat, n_feat, kernel_size,dilation=3, padding=3,bias=bias)     
        )
        self.conv = nn.Sequential(nn.Conv2d(3*n_feat, n_feat, kernel_size,padding=1,bias=bias))
        modules_ca.append(RC_CALayer(n_feat, reduction))
        self.ca = nn.Sequential(*modules_ca)
        self.res_scale = res_scale

    def forward(self, x):
        res1 = self.body1(x)
        res2 = self.body2(x)
        res3 = self.body3(x)
        res = torch.cat([res1,res2,res3],1)
        res = self.conv(res)
        res = self.ca(res)
        #res = self.body(x).mul(self.res_scale)
        res = res+ x
        return res

        
## Residual Group (RG)
class ResidualGroup(nn.Module):
    def __init__(self, conv, n_feat, kernel_size, reduction, act, res_scale, n_resblocks):
        super(ResidualGroup, self).__init__()
        modules_body = []
        modules_body = [
            RCAB(
                conv, n_feat, kernel_size, reduction, bias=True, bn=False, act=nn.ReLU(True), res_scale=1) \
            for _ in range(n_resblocks)]
        modules_body.append(conv(n_feat, n_feat, kernel_size))
        self.body = nn.Sequential(*modules_body)

    def forward(self, x):
        res = self.body(x)
        res = res+ x
        return res


class rcan(nn.Module):
    def __init__(self, conv=common.default_conv):
        super(rcan, self).__init__()
        n_resgroups = 3
        n_resblocks = 6
        n_feats = 32
        kernel_size = 3
        reduction = 8
        act = nn.ReLU(True)

        # self.conv1 = nn.Conv2d(3, n_feats, kernel_size=3, padding=1, bias=True)
        modules_head = [conv(3, n_feats, kernel_size)]

        # define body module
        modules_body = [
            ResidualGroup(
                conv, n_feats, kernel_size, reduction, act=act, res_scale=1, n_resblocks=n_resblocks) \
            for _ in range(n_resgroups)]

        modules_body.append(conv(n_feats, n_feats, kernel_size))

        # define tail module
        modules_tail = [
            conv(n_feats, 3, kernel_size)]

        self.head = nn.Sequential(*modules_head)
        self.body = nn.Sequential(*modules_body)
        self.tail = nn.Sequential(*modules_tail)

    def forward(self, x):
        x_feat = self.head(x)
        res = self.body(x_feat)
        out_feat = res + x_feat
        return out_feat


